package com.company.studentDetails;

public class Student {
    public String studentName;
    public String sex;
    public int rollNum;
    public int age;

    public Student(String studentName, String sex, int rollNum, int age) {
        this.studentName = studentName;
        this.sex = sex;
        this.rollNum = rollNum;
        this.age = age;
    }
}
